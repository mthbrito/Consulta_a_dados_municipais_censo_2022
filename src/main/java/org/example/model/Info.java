package org.example.model;

public class Info {

    private String infoData;
    private String infoRows;

    public Info() {
    }

    public String getInfoData() {
        return infoData;
    }

    public void setInfoData(String infoData) {
        this.infoData = infoData;
    }

    public String getInfoRows() {
        return infoRows;
    }

    public void setInfoRows(String infoRows) {
        this.infoRows = infoRows;
    }
}
